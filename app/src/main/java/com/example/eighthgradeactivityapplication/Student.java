package com.example.eighthgradeactivityapplication;

public class Student {

    int studentID;
    String studentFirstName;
    String studentLastName;
    String studentItem1;
    String studentItem2;
    String studentItem1Price;
    String studentItem2Price;

    public Student() {
        studentID = -1;
    }

    public int getStudentID() {
        return studentID;
    }

    public void setStudentID(int studentID) {
        this.studentID = studentID;
    }

    public String getStudentFirstName() {
        return studentFirstName;
    }

    public void setStudentFirstName(String studentFirstName) {
        this.studentFirstName = studentFirstName;
    }

    public String getStudentLastName() {
        return studentLastName;
    }

    public void setStudentLastName(String studentLastName) {
        this.studentLastName = studentLastName;
    }

    public String getStudentItem1() {
        return studentItem1;
    }

    public void setStudentItem1(String studentItem1) {
        this.studentItem1 = studentItem1;
    }

    public String getStudentItem2() {
        return studentItem2;
    }

    public void setStudentItem2(String studentItem2) {
        this.studentItem2 = studentItem2;
    }

    public String getStudentItem1Price() {
        return studentItem1Price;
    }

    public void setStudentItem1Price(String studentItem1Price) {
        this.studentItem1Price = studentItem1Price;
    }

    public String getStudentItem2Price() {
        return studentItem2Price;
    }

    public void setStudentItem2Price(String studentItem2Price) {
        this.studentItem2Price = studentItem2Price;
    }
}


